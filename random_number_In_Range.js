// Write a JavaScript program to get a random number in the specified range.
const random_Number_In_Range = (min, max) => Math.random() * (max - min) + min;

console.log(random_Number_In_Range(2, 10)); // 5.897778190464821
console.log(random_Number_In_Range(1, 5)); // 1.1596294444724986
console.log(random_Number_In_Range(-5, -2)); // -2.644812110240778
console.log(random_Number_In_Range(0, 1)); // 0.5654096114937668

